
# Transformação

frase = 'Teste de como funciona'
print(frase.replace('Teste', 'Eita')) # vai trocar o selecionado
print(frase.upper()) # coloca tudo em caps
print(frase.lower()) # coloca tudo minusculo
print(frase.capitalize()) # ele deixa só o primeiro caractere maisculo
print(frase.title()) # ele analise e coloca a primeira letra de cada palavra em maiusculo
print(frase.strip()) # ele remove os espaços inuteis no começo e fim da string
print(frase.rstrip()) # ele remove os espaços inuteis do fim right
print(frase.lstrip()) # ele remove os espaços inuteis do inicio left